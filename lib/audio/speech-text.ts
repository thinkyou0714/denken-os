/**
 * speech-text.ts — 問題テキストを TTS(音声合成)が正しく読める日本語へ正規化する。
 *
 * 音声学習の最大の品質リスクは「単位・記号・数式・専門用語の誤読」。
 *   例: "150/Ig" を「ひゃくごじゅう スラッシュ アイジー」、"Ω" を無音、
 *       "√(8²+6²)" を意味不明に、"力率" を「ちからりつ」と読むと教材にならない。
 * ここで読み上げ用の表記へ機械的に変換する（純関数・決定論。新情報は足さない）。
 *
 * 設計: 順序付き置換。
 *   1) 専門用語の読み辞書（漢字→かな。長い語から）
 *   2) 単位（数字の直後 or 〔〕内のみ。変数名 V_p/R/P は壊さない。長い単位から）
 *   3) 上付き指数（10⁻⁶ → 「の マイナス6 乗」）
 *   4) 演算子・記号・ギリシャ文字
 */

