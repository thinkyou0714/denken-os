/**
 * speech-text.ts — 問題テキストを TTS(音声合成)が正しく読める日本語へ正規化する。
 *
 * 音声学習の最大の品質リスクは「単位・記号・数式の誤読」。
 *   例: "150/Ig" を「ひゃくごじゅう スラッシュ アイジー」、"Ω" を無音、
 *       "√(8²+6²)" を意味不明に読むと、聞き流し教材として成立しない。
 * ここで読み上げ用の表記へ機械的に変換する（純関数・決定論。新情報は足さない）。
 *
 * 設計: 順序付き置換。複数文字の単位を単一文字より先に処理する
 *       （例: "MVA" を "V"/"A" より先に）。単位は「数字の直後」または〔〕内のみ
 *       変換し、変数名(V_p, R, P 等)は壊さない。
 */

/** 単位記号 → 読み（長いものから順に適用する）。 */
const UNITS: ReadonlyArray<readonly [string, string]> = [
  ["min⁻¹", "毎分回転"],
  ["MVA", "メガボルトアンペア"],
  ["kVA", "キロボルトアンペア"],
  ["kV", "キロボルト"],
  ["MΩ", "メガオーム"],
  ["kΩ", "キロオーム"],
  ["kW", "キロワット"],
  ["mJ", "ミリジュール"],
  ["μF", "マイクロファラド"],
  ["Hz", "ヘルツ"],
  ["Ω", "オーム"],
  ["W", "ワット"],
  ["V", "ボルト"],
  ["A", "アンペア"],
  ["J", "ジュール"],
  ["F", "ファラド"],
  ["%", "パーセント"],
];

const UNIT_MAP = new Map(UNITS.map(([u, s]) => [u, s]));

/** 記号・演算子 → 読み（順序が意味を持つ）。 */
const SYMBOLS: ReadonlyArray<readonly [RegExp, string]> = [
  [/cos/g, "コサイン"],
  [/sin/g, "サイン"],
  [/tan/g, "タンジェント"],
  [/√/g, "ルート"],
  [/²/g, "の2乗"],
  [/³/g, "の3乗"],
  [/[×・]/g, "かける"],
  [/÷/g, "わる"],
  [/\//g, "わる"],
  [/[＝=]/g, "イコール"],
  [/[≈≒]/g, "およそ"],
  [/[≦≤]/g, "以下"],
  [/[≧≥]/g, "以上"],
  [/±/g, "プラスマイナス"],
  [/[−-]/g, "ひく"],
  [/\+/g, "たす"],
  [/μ/g, "マイクロ"],
  [/φ/g, "ファイ"],
  [/θ/g, "シータ"],
  [/Δ/g, "デルタ"],
  [/Σ/g, "シグマ"],
  [/π/g, "パイ"],
  [/ω/g, "オメガ"],
  [/λ/g, "ラムダ"],
  [/[()]/g, " "],
];

function escapeRegExp(s: string): string {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

/**
 * 問題文・解説・選択肢などの1テキストを、音声合成向けの読みに正規化する。
 */
export function toSpeech(text: string): string {
  let out = text;

  // 下付き(V_p → Vp)・上付き由来の装飾を素直化。
  out = out.replace(/_/g, "");

  // 虚数単位 j（数字の直前）→「ジェイ」。"8+j6" → "8+ジェイ6"。
  out = out.replace(/j(?=\d)/g, "ジェイ");

  // 〔単位〕表記 → 「、<読み>」。未知の中身はそのまま残す。
  out = out.replace(/〔\s*([^〕]*?)\s*〕/g, (_m, inner: string) => {
    const spoken = UNIT_MAP.get(inner);
    return spoken ? `、${spoken}` : inner;
  });

  // 数字の直後の単位（長いものから）。変数名は数字が前置しないため壊れない。
  for (const [u, spoken] of UNITS) {
    out = out.replace(new RegExp(`(\\d)\\s*${escapeRegExp(u)}`, "g"), `$1${spoken}`);
  }

  // 記号・演算子。
  for (const [re, rep] of SYMBOLS) {
    out = out.replace(re, rep);
  }

  // 空白を整える。
  return out.replace(/\s+/g, " ").trim();
}
